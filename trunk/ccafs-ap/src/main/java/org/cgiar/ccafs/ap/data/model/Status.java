package org.cgiar.ccafs.ap.data.model;


public class Status {

  private int id;
  private String name;

  public Status() {
  }

  public int getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public void setId(int id) {
    this.id = id;
  }

  public void setName(String name) {
    this.name = name;
  }

}
